#include "libft.h"

int	ft_atoi(const char *str)
{
	int					sign;
	int					i;
	unsigned long long	res;

	i = 0;
	res = 0;
	sign = 1;
	while ((str[i] >= 9 && str[i] <= 13) || (str[i] == 32))
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			sign *= -1;
		i++;
	}
	while (str[i] >= 48 && str[i] <= 57)
	{
		res = res * 10 + (str[i] - 48);
		i++;
		if (res > LONG_MAX && sign == -1)
			return (0);
		if (res > LONG_MAX)
			return (-1);
	}
	return (res * sign);
}
